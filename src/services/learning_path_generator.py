    def generate(self, risks, all_matches):
        """生成完整的补差路线文本"""
        if not risks:
            return self._no_gaps_plan()
        lines = []
        lines.append("### 当前技能差距")
        for i, m in enumerate(risks[:5], 1):
            t = m.requirement.replace("具备 ", "").replace(" 相关能力", "").replace(" 相关经验优先", "")
            lines.append(f"{i}. {t}：{m.reason}")
        lines.append("")
        lines.append("### 7天补强路线")
        used = set()
        for m in risks[:3]:
            for keyword, plan in self.CUSTOM_PLANS.items():
                if keyword.lower() in m.requirement.lower() or any(
                    k in m.requirement for k in [keyword]
                ):
                    if keyword not in used:
                        used.add(keyword)
                        lines.append("")
                        for s in plan:
                            lines.append(s)
                    break
        if not used:
            lines.append("")
            for s in self.GENERAL_PLAN:
                lines.append(s)
        lines.append("")
        lines.append("### 14天拓展路线")
        for s in self.EXTEND_14d:
            lines.append(s)
        lines.append("")
        lines.append("### 30天进阶路线")
        for s in self.EXTEND_30d:
            lines.append(s)
        return "\n".join(lines)

